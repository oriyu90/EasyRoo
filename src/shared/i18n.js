  const SUPPORTED = ['ja', 'en'];
  const FALLBACK = 'en';
  let current = 'ja';

  /** OS/ブラウザのロケールから対応言語を決める。未対応なら英語。 */
  function detect(rawLocale) {
    const l = String(rawLocale || '').toLowerCase();
    for (const code of SUPPORTED) {
      if (l.startsWith(code)) return code;
    }
    return FALLBACK;
  }

  function resolve(pref, systemLocale) {
    if (SUPPORTED.includes(pref)) return pref;
    return detect(systemLocale);
  }

  function setLanguage(lang) {
    current = SUPPORTED.includes(lang) ? lang : FALLBACK;
    return current;
  }

  function getLanguage() {
    return current;
  }

  /**
   * 訳文を引く。{name} 形式のプレースホルダを params で置換する。
   * キーが無い場合は英語へ、それも無ければキー自体を返す(画面が壊れないようにする)。
   */
  function t(key, params) {
    const table = DICT[current] || DICT[FALLBACK];
    let s = table[key];
    if (s === undefined) s = DICT[FALLBACK][key];
    if (s === undefined) return key;
    if (params) {
      s = s.replace(/\{(\w+)\}/g, (m, k) => (params[k] === undefined ? m : String(params[k])));
    }
    return s;
  }

  /** 指定言語で引く(通知など、現在言語に依存させたくない場面用) */
  function tIn(lang, key, params) {
    const prev = current;
    setLanguage(lang);
    const s = t(key, params);
    current = prev;
    return s;
  }

  return { t, tIn, setLanguage, getLanguage, detect, resolve, SUPPORTED, DICT };
